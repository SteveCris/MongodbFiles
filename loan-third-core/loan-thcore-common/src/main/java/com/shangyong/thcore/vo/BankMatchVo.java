package com.shangyong.thcore.vo;

import java.io.Serializable;

public class BankMatchVo implements Serializable {


    /**
	 * 
	 */
	private static final long serialVersionUID = -9216548067384940807L;

	//银行代码
    private String bankCode;

    //银行名称
    private String bankName;

    //卡号前缀
    private String cardNoPrefix;

	public String getBankCode() {
		return bankCode;
	}

	public void setBankCode(String bankCode) {
		this.bankCode = bankCode;
	}

	public String getBankName() {
		return bankName;
	}

	public void setBankName(String bankName) {
		this.bankName = bankName;
	}

	public String getCardNoPrefix() {
		return cardNoPrefix;
	}

	public void setCardNoPrefix(String cardNoPrefix) {
		this.cardNoPrefix = cardNoPrefix;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("BankMatchVo [bankCode=");
		builder.append(bankCode);
		builder.append(", bankName=");
		builder.append(bankName);
		builder.append(", cardNoPrefix=");
		builder.append(cardNoPrefix);
		builder.append("]");
		return builder.toString();
	}
    
    
}
