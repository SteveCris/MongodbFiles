package com.shangyong.thcore.dto;

import java.io.Serializable;

/**
 * Created by ybds on 2019-03-27.
 */
public class UserQuarantineDto implements Serializable {

    private static final long serialVersionUID = -4344936723017038723L;

    /**
     * 应用appid
     */
    private String appid;
    /**
     * 手机+身份证(大写)的md5值
     */
    private String phoneIdNumberMd5;

    public String getAppid() {
        return appid;
    }

    public void setAppid(String appid) {
        this.appid = appid;
    }

    public String getPhoneIdNumberMd5() {
        return phoneIdNumberMd5;
    }

    public void setPhoneIdNumberMd5(String phoneIdNumberMd5) {
        this.phoneIdNumberMd5 = phoneIdNumberMd5;
    }

    @Override
    public String toString() {
        final StringBuilder sb = new StringBuilder("UserQuarantineDto{");
        sb.append("appid='").append(appid).append('\'');
        sb.append(", phoneIdNumberMd5='").append(phoneIdNumberMd5).append('\'');
        sb.append('}');
        return sb.toString();
    }
}
