package com.shangyong.thcore.bo;

import java.io.Serializable;
import java.util.Date;

public class OrderRepaymentBo implements Serializable {



	/**
	 * 
	 */
	private static final long serialVersionUID = 8016911952027324878L;

	private Date createTime;
	
	private boolean ifOverdue;
	
	private String applyId;
	
	private String financeOrderId;

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public boolean isIfOverdue() {
		return ifOverdue;
	}

	public void setIfOverdue(boolean ifOverdue) {
		this.ifOverdue = ifOverdue;
	}

	public String getApplyId() {
		return applyId;
	}

	public void setApplyId(String applyId) {
		this.applyId = applyId;
	}

	public String getFinanceOrderId() {
		return financeOrderId;
	}

	public void setFinanceOrderId(String financeOrderId) {
		this.financeOrderId = financeOrderId;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("OrderRepaymentBo [createTime=");
		builder.append(createTime);
		builder.append(", ifOverdue=");
		builder.append(ifOverdue);
		builder.append(", applyId=");
		builder.append(applyId);
		builder.append(", financeOrderId=");
		builder.append(financeOrderId);
		builder.append("]");
		return builder.toString();
	}
}
