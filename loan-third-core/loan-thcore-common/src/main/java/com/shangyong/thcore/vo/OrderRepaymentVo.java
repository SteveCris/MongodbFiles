package com.shangyong.thcore.vo;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.List;

public class OrderRepaymentVo implements Serializable {

	

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	private BigDecimal totalRepaymentAmount;
	
	private OrderRuleVo orderRuleVo;
	
	private OrderBankVo orderBankVo;
	
	private List<RepaymentTypeVo> repaymentTypeVoList;

	public BigDecimal getTotalRepaymentAmount() {
		return totalRepaymentAmount;
	}

	public void setTotalRepaymentAmount(BigDecimal totalRepaymentAmount) {
		this.totalRepaymentAmount = totalRepaymentAmount;
	}

	public OrderRuleVo getOrderRuleVo() {
		return orderRuleVo;
	}

	public void setOrderRuleVo(OrderRuleVo orderRuleVo) {
		this.orderRuleVo = orderRuleVo;
	}

	public OrderBankVo getOrderBankVo() {
		return orderBankVo;
	}

	public void setOrderBankVo(OrderBankVo orderBankVo) {
		this.orderBankVo = orderBankVo;
	}

	public List<RepaymentTypeVo> getRepaymentTypeVoList() {
		return repaymentTypeVoList;
	}

	public void setRepaymentTypeVoList(List<RepaymentTypeVo> repaymentTypeVoList) {
		this.repaymentTypeVoList = repaymentTypeVoList;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("OrderRepaymentVo [totalRepaymentAmount=");
		builder.append(totalRepaymentAmount);
		builder.append(", orderRuleVo=");
		builder.append(orderRuleVo);
		builder.append(", orderBankVo=");
		builder.append(orderBankVo);
		builder.append(", repaymentTypeVoList=");
		builder.append(repaymentTypeVoList);
		builder.append("]");
		return builder.toString();
	}



	
	
}
