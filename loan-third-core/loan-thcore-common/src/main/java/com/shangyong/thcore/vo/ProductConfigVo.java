package com.shangyong.thcore.vo;

import java.io.Serializable;
import java.math.BigDecimal;

public class ProductConfigVo implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -6385673002424765006L;

	// 应用id
	private String appid;

	// 产品编号
	private String code;

	// 备注
	private String remark;

	// 产品名称
	private String name;

	// 产品价格
	private BigDecimal price;

	// 周期
	private Integer cycle;

	// 日利率
	private BigDecimal dayRate;

	// 前置产品价格
	private BigDecimal prePrice;

	// 期数
	private Integer periods;

	// 应用标识
	private String appName;

	// 应用名称
	private String applicationName;

	// 类别
	private int type;
	
	private String ext;
	
	private String ext2;

	public String getAppid() {
		return appid;
	}

	public void setAppid(String appid) {
		this.appid = appid;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public BigDecimal getPrice() {
		return price;
	}

	public void setPrice(BigDecimal price) {
		this.price = price;
	}

	public Integer getCycle() {
		return cycle;
	}

	public void setCycle(Integer cycle) {
		this.cycle = cycle;
	}

	public BigDecimal getDayRate() {
		return dayRate;
	}

	public void setDayRate(BigDecimal dayRate) {
		this.dayRate = dayRate;
	}

	public BigDecimal getPrePrice() {
		return prePrice;
	}

	public void setPrePrice(BigDecimal prePrice) {
		this.prePrice = prePrice;
	}

	public Integer getPeriods() {
		return periods;
	}

	public void setPeriods(Integer periods) {
		this.periods = periods;
	}

	public String getAppName() {
		return appName;
	}

	public void setAppName(String appName) {
		this.appName = appName;
	}

	public String getApplicationName() {
		return applicationName;
	}

	public void setApplicationName(String applicationName) {
		this.applicationName = applicationName;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public String getExt() {
		return ext;
	}

	public void setExt(String ext) {
		this.ext = ext;
	}

	public String getExt2() {
		return ext2;
	}

	public void setExt2(String ext2) {
		this.ext2 = ext2;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("ProductConfigVo [appid=");
		builder.append(appid);
		builder.append(", code=");
		builder.append(code);
		builder.append(", remark=");
		builder.append(remark);
		builder.append(", name=");
		builder.append(name);
		builder.append(", price=");
		builder.append(price);
		builder.append(", cycle=");
		builder.append(cycle);
		builder.append(", dayRate=");
		builder.append(dayRate);
		builder.append(", prePrice=");
		builder.append(prePrice);
		builder.append(", periods=");
		builder.append(periods);
		builder.append(", appName=");
		builder.append(appName);
		builder.append(", applicationName=");
		builder.append(applicationName);
		builder.append(", type=");
		builder.append(type);
		builder.append(", ext=");
		builder.append(ext);
		builder.append(", ext2=");
		builder.append(ext2);
		builder.append("]");
		return builder.toString();
	}

}
