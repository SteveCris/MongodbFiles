package com.shangyong.thcore.vo;

import java.io.Serializable;

public class BaseUserInfoVo implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2847790580369531344L;

	// 身份证号码
	private String identityNo;

	// 用户名称
	private String userName;

	// 手机号码
	private String mobile;

	// 来源订单id
	private String orderId;

	// 来源第三方订单id
	private String otherOrderId;

	// 来源应用标识
	private String appName;

	// 来源应用id
	private String appid;

	/**
	 * 是否存在用户
	 */
	private boolean ifExistUser;

	/**
	 * 是否处于隔离期
	 */
	private boolean ifQuarantine;

	/**
	 * 隔离结束时间
	 */
	private String quarantineEndTime;

	/**
	 * 隔离原因
	 */
	private String reason;

	public String getIdentityNo() {
		return identityNo;
	}

	public void setIdentityNo(String identityNo) {
		this.identityNo = identityNo;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getOrderId() {
		return orderId;
	}

	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}

	public String getOtherOrderId() {
		return otherOrderId;
	}

	public void setOtherOrderId(String otherOrderId) {
		this.otherOrderId = otherOrderId;
	}

	public String getAppName() {
		return appName;
	}

	public void setAppName(String appName) {
		this.appName = appName;
	}

	public String getAppid() {
		return appid;
	}

	public void setAppid(String appid) {
		this.appid = appid;
	}

	public boolean isIfExistUser() {
		return ifExistUser;
	}

	public void setIfExistUser(boolean ifExistUser) {
		this.ifExistUser = ifExistUser;
	}

	public boolean isIfQuarantine() {
		return ifQuarantine;
	}

	public void setIfQuarantine(boolean ifQuarantine) {
		this.ifQuarantine = ifQuarantine;
	}

	public String getQuarantineEndTime() {
		return quarantineEndTime;
	}

	public void setQuarantineEndTime(String quarantineEndTime) {
		this.quarantineEndTime = quarantineEndTime;
	}

	public String getReason() {
		return reason;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("BaseUserInfoVo [identityNo=");
		builder.append(identityNo);
		builder.append(", userName=");
		builder.append(userName);
		builder.append(", mobile=");
		builder.append(mobile);
		builder.append(", orderId=");
		builder.append(orderId);
		builder.append(", otherOrderId=");
		builder.append(otherOrderId);
		builder.append(", appName=");
		builder.append(appName);
		builder.append(", appid=");
		builder.append(appid);
		builder.append(", ifExistUser=");
		builder.append(ifExistUser);
		builder.append(", ifQuarantine=");
		builder.append(ifQuarantine);
		builder.append(", quarantineEndTime=");
		builder.append(quarantineEndTime);
		builder.append(", reason=");
		builder.append(reason);
		builder.append("]");
		return builder.toString();
	}

}
