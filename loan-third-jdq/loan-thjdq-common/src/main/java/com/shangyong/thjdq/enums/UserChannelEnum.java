public enum UserChannelEnum {

    SY("SY","尚咏"),
    JDQ("new_mysdj","借点钱");

    private String name;
    private String desc;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    UserChannelEnum(String name, String desc) {
        this.name = name;
        this.desc = desc;
    }

}
