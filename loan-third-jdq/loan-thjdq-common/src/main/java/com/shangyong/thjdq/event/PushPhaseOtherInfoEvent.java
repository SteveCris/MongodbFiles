package com.shangyong.thjdq.event;

import java.io.Serializable;

/**
 * 用户其他相关信息入库事件
 * Created by ybds on 2019-03-19.
 */
public class PushPhaseOtherInfoEvent implements Serializable{

    private static final long serialVersionUID = 8230313989346135644L;

    private String jdqOrderId;

    public String getJdqOrderId() {
        return jdqOrderId;
    }

    public void setJdqOrderId(String jdqOrderId) {
        this.jdqOrderId = jdqOrderId;
    }

    @Override
    public String toString() {
        final StringBuilder sb = new StringBuilder("PushPhaseOtherInfoEvent{");
        sb.append("jdqOrderId=").append(jdqOrderId);
        sb.append('}');
        return sb.toString();
    }
}
