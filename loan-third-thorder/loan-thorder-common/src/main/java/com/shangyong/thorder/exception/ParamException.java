package com.shangyong.thorder.exception;

/**
 * 自定义参数异常
 * 
 * @author zhangze
 */
public class ParamException extends RuntimeException {

	private static final long serialVersionUID = -2220962137480554310L;

	public ParamException() {
		super();
	}

	public ParamException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ParamException(String message, Throwable cause) {
		super(message, cause);
	}

	public ParamException(String message) {
		super(message);
	}

	public ParamException(Throwable cause) {
		super(cause);
	}

	
	
}
